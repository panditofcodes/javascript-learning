x = 90;
y = 30;
z = 3;

if (x + y === z) {
  console.log(`${x} + ${y} = ${z}`);
} else if (x - y === z) {
  console.log(`${x} - ${y} = ${z}`);
} else if (x * y === z) {
  console.log(`${x} * ${y} = ${z}`);
} else if (x / y === z) {
  console.log(`${x} / ${y} = ${z}`);
} else {
  console.log("Unknown");
}
