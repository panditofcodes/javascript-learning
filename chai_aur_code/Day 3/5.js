let score = 95;
switch (true) {
    case score >= 95:
        console.log("A grade");
        break;
    case score >= 85:
        console.log("B grade");
        break;
    case score >= 75:
        console.log("C grade");
        break;
    case score >= 65:
        console.log("D grade");
        break;
    case score >= 55:
        console.log("E grade");
        break;
    case score >= 45:
        console.log("F grade");
        break;
    default:
        console.log("Fail");
        break;
}
